  <p class="c0"><span class="c4 c2">4.5.</span><span class="c1">&nbsp;Voit valita, haluatko rekister&ouml;id&auml; muille n&auml;kyv&auml;ksi k&auml;ytt&auml;j&auml;nimeksesi oman nimesi vai anonyymin tunnuksen. </span></p>
  <p class="c0"><span class="c5 c4">5. TIETOTURVA</span></p>

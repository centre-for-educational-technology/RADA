  <p class="c0" id="h.gjdgxs"><span class="c5 c4">1. RADA-pelin TIETOSUOJASELOSTE</span></p>
  <p class="c0"><span class="c2">RADA-pelist&auml;</span><span class="c2 c4">&nbsp;</span><span class="c1">(RADA.eu / &ldquo;Alusta&rdquo;) vastaa Tallinnan yliopisto RADA-projektin puolesta. T&auml;m&auml;n tietosuojaselosteen tarkoitus on selitt&auml;&auml;, miten RADA-peli (&rdquo;RADA&rdquo; tai &rdquo;me&rdquo;) ker&auml;&auml;, k&auml;ytt&auml;&auml; ja jakaa sinua koskevaa tietoa tarjoamamme Alustan ja palvelujen kautta.</span></p>
  <p class="c0"><span class="c1">T&auml;m&auml; seloste k&auml;sittelee RADA-pelin tietosuojaa. RADA-nimityksell&auml; tarkoitetaan t&auml;ss&auml; Alustaa ja siihen liittyvi&auml; palveluja. RADA ker&auml;&auml; k&auml;ytt&auml;jist&auml; erilaisia tietoja: henkil&ouml;tietoja, lokitietoja ja henkil&ouml;&ouml;n kohdistumatonta tietoa sek&auml; tietoa k&auml;ytt&auml;jien tekemist&auml; toimista.</span></p>
  <p class="c0"><span class="c1">RADA-alusta ja siihen liittyv&auml;t palvelut takaavat, ett&auml; tietoja ker&auml;t&auml;&auml;n ja tallennetaan vain RADA-alustan omaan k&auml;ytt&ouml;&ouml;n ja ett&auml; niit&auml; k&auml;sitell&auml;&auml;n EU:n tietosuoja-asetuksen mukaisesti.</span></p>
  <p class="c0"><span class="c1">Ennen Alustan k&auml;ytt&ouml;&auml; k&auml;ytt&auml;jien on hyv&auml;ksytt&auml;v&auml; RADAin tietosuojaseloste, joka kertoo tietojen k&auml;yt&ouml;st&auml; ja s&auml;&auml;ntelee sit&auml; sek&auml; kertoo, mihin RADA-alustan seurantapalveluja k&auml;ytet&auml;&auml;n.</span></p>
  <p class="c0"><span class="c1">RADA-alustan tietosuojaseloste koskee kaikkia RADA-pelin k&auml;ytt&auml;ji&auml; &ndash; miss&auml; tahansa k&auml;ytt&auml;j&auml;roolissa &ndash; jotka rekister&ouml;ityv&auml;t RADA-alustalle tai pelaavat peli&auml; anonyymisti. RADA on tehnyt kaiken voitavansa varmistaakseen, ett&auml; k&auml;ytt&auml;jien henkil&ouml;tietojen luvattoman k&auml;yt&ouml;n est&auml;v&auml;t tekniset ja hallinnolliset turvatoimenpiteet on tehty.</span></p>
  <p class="c0"><span class="c1">RADA-alusta voi sis&auml;lt&auml;&auml; linkkej&auml; muiden sis&auml;ll&ouml;ntuottajien julkaisemille ulkoisille verkkosivuille. Emme hallinnoi n&auml;it&auml; ulkoisia verkkosivuja ja suosittelemme k&auml;ytt&auml;ji&auml; tarkistamaan kunkin k&auml;ytt&auml;m&auml;ns&auml; verkkosivun tietosuojaselosteen erikseen.</span></p>
  <p class="c0"><span class="c4 c2">1.1 Henkil&ouml;tietojen m&auml;&auml;ritelm&auml;</span></p>
  <p class="c0"><span class="c1">Henkil&ouml;tiedot ovat RADA-alustalle antamiasi tietoja, joista olet henkil&ouml;kohtaisesti tunnistettavissa, kuten nimi tai s&auml;hk&ouml;postiosoite, tai muita tietoja, jotka voidaan j&auml;rkev&auml;sti yhdist&auml;&auml; t&auml;llaiseen tietoon. Kun rekister&ouml;idyt k&auml;ytt&auml;j&auml;ksi, annat meille henkil&ouml;tietojasi.</span></p>
  <p class="c0"><span class="c1">RADA noudattaa henkil&ouml;tietojen ker&auml;&auml;miseen ja k&auml;sittelyyn liittyvi&auml; eurooppalaisia lakeja, ja RADA pyrkii erityisesti noudattamaan OECD Privacy Framework -ohjeistusta (OECD 2013):</span></p>
  <ol class="c9 lst-kix_list_6-0 start" start="1">
    <li class="c0 c3"><span class="c1">Tiedot on hankittava luvallisin ja oikeudenmukaisin keinoin ja soveltuvissa m&auml;&auml;rin tietojen kohteen suostumuksella; </span></li>
    <li class="c0 c3"><span class="c1">Henkil&ouml;tietoja ker&auml;t&auml;&auml;n vain asiaankuuluvaan tarkoitukseen ja vain siin&auml; m&auml;&auml;rin kuin niit&auml; tarvitaan t&auml;h&auml;n tarkoitukseen. Tietojen tulee olla t&auml;sm&auml;llisi&auml;, t&auml;ydellisi&auml; ja ajan tasalla;</span></li>
    <li class="c0 c3"><span class="c1">Tarkoitukset, joita varten henkil&ouml;tietoja ker&auml;t&auml;&auml;n, on m&auml;&auml;ritelt&auml;v&auml; viimeist&auml;&auml;n tietojen ker&auml;ysvaiheessa;</span></li>
    <li class="c0 c3"><span class="c1">Henkil&ouml;tietoja ei saa paljastaa tai k&auml;ytt&auml;&auml; muihin tarkoituksiin kuin siihen, jota varten ne on ker&auml;tty, eik&auml; niit&auml; saa antaa muiden k&auml;ytt&ouml;&ouml;n &ndash; paitsi tietojen kohteen suostumuksella tai viranomaisten m&auml;&auml;r&auml;yksest&auml;;</span></li>
    <li class="c0 c3"><span class="c1">Henkil&ouml;tiedot on suojattava kohtuullisin turvatoimenpitein h&auml;vitt&auml;misen tai luvattoman k&auml;yt&ouml;n, tuhoamisen, muuttamisen tai paljastamisen varalta;</span></li>
    <li class="c0 c3"><span class="c1">Tietoa on oltava saatavilla henkil&ouml;tietojen olemassaolosta ja laadusta, niiden k&auml;ytt&ouml;tarkoituksesta sek&auml; rekisterinpit&auml;j&auml;st&auml; ja t&auml;m&auml;n sijainnista.</span></li>
  </ol>
  <p class="c0"><span class="c1">Lue t&auml;m&auml; seloste huolellisesti ymm&auml;rt&auml;&auml;ksesi, mit&auml; teemme tiedoilla, joita j&auml;t&auml;t meille k&auml;ytt&auml;m&auml;ll&auml; RADA-alustaa. </span></p>
  <p class="c0"><span class="c2">Jos t&auml;ss&auml; tietosuojaselosteessa on jotakin, jota et ymm&auml;rr&auml;, ota meihin yhteytt&auml; osoitteessa </span><span class="c10 c13">info@RADA.eu</span></p>

  <p class="c0"><span class="c4 c2">3.1.2</span><span class="c1">&nbsp;Rekister&ouml;ityminen ei ole mahdollista, jos k&auml;ytt&auml;j&auml; ei hyv&auml;ksy tietosuojaselostetta ja k&auml;ytt&ouml;ehtoja. </span></p>
  <p class="c0"><span class="c4 c2">3.1.3</span><span class="c1">&nbsp;Alustalla voi joskus olla linkkej&auml; kolmansien osapuolten verkkosivuille. Jos seuraat linkki&auml; jollekin n&auml;ist&auml; verkkosivuista, ota huomioon, ett&auml; n&auml;ill&auml; verkkosivuilla on omat tietosuojaselosteensa ja ett&auml; me emme ole vastuussa niiden tietosuojasta. Tarkista muiden verkkosivujen tietosuojaselosteet ennen kuin annat n&auml;ille sivuille henkil&ouml;tietoja. </span></p>
  <p class="c0"><span class="c4 c5">4. K&Auml;YTT&Auml;J&Auml;N OIKEUDET </span></p>
  <p class="c0"><span class="c4 c2">4.1.</span><span class="c1">&nbsp;Sinulla on oikeus tiet&auml;&auml; selke&auml;ll&auml; ja ymm&auml;rrett&auml;v&auml;ll&auml; tavalla, millaista tietoa sinusta ker&auml;t&auml;&auml;n, mit&auml; tarkoitusta varten sit&auml; ker&auml;t&auml;&auml;n ja miten tietojasi k&auml;sitell&auml;&auml;n. </span></p>

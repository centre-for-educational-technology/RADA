  <p class="c0"><span class="c5 c4">3. SUOSTUMUS </span></p>
  <p class="c0"><span class="c4 c2">3.1.</span><span class="c1">&nbsp;RADA-alusta sijaitsee Virossa olevilla palvelimilla. Kaikki tiedot, jotka koskevat Alustalle rekister&ouml;itymist&auml;, ev&auml;steit&auml; ja pelin tuloksia, kootaan ja tallennetaan n&auml;ille palvelimille. Koska Viro kuuluu Euroopan talousalueeseen (ETA), se noudattaa ETA:n tietosuojaa ja henkil&ouml;tietojen suojelua koskevia lakeja. K&auml;ytt&ouml;tietoja, jotka liittyv&auml;t enimm&auml;kseen analytiikkaan ja virheiden j&auml;ljitt&auml;miseen asiakasohjelmassa, voidaan s&auml;ilytt&auml;&auml; my&ouml;s EU:hun kuulumattomissa maissa (kuten Sveitsiss&auml;) tai Euroopan ulkopuolisissa maissa (kuten Yhdysvalloissa). Kaikki n&auml;m&auml; tiedot ovat anonyymej&auml; eik&auml; niit&auml; voida suoraan yhdist&auml;&auml; Alustan k&auml;ytt&auml;jiin.</span></p>

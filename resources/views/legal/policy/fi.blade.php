  <ol class="c9 lst-kix_list_4-0 start" start="1">
    <li class="c0 c3"><span class="c1">P&auml;&auml;synvalvonta todennetaan salasanalla &ndash; kaikkien j&auml;rjestelm&auml;&auml; k&auml;ytt&auml;vien yll&auml;pit&auml;jien t&auml;ytyy k&auml;ytt&auml;&auml; salasanoja; </span></li>
    <li class="c0 c3"><span class="c1">Palomuuri on k&auml;yt&ouml;ss&auml;; </span></li>
    <li class="c0 c3"><span class="c1">SSL (Secure Sockets Layer) -protokolla on asennettu (katso kohta 7); </span></li>
    <li class="c0 c3"><span class="c1">J&auml;rjestelm&auml; varmuuskopioidaan s&auml;&auml;nn&ouml;llisesti</span></li>
  </ol>
  <p class="c0"><span class="c4 c2">5.2</span><span class="c1">. RADA suojelee kaikkea tallennettua tietoa v&auml;&auml;rink&auml;yt&ouml;lt&auml; ja kunnioittaa tietojen omistajien oikeuksia, jotka EU:n tietosuoja-asetus (GDPR) takaa.</span></p>
  <p class="c0"><span class="c4 c2">5.3.</span><span class="c1">&nbsp;RADA suojelee kaikkia henkil&ouml;tietoja riippumatta siit&auml;, minne ne l&auml;hetet&auml;&auml;n tai miss&auml; niit&auml; k&auml;sitell&auml;&auml;n tai varastoidaan, jopa EU:n ulkopuolella, ja noudattaa sen vuoksi EU:n tavanomaisia sopimuslausekkeita.</span></p>
  <p class="c0"><span class="c4 c2">5.5</span><span class="c1">. RADA voi luovuttaa henkil&ouml;tietojasi muille vain seuraavissa tapauksissa: <br>(a) jos meit&auml; vaaditaan luovuttamaan henkil&ouml;tietoja lain, asetuksen tai muun viranomaisk&auml;skyn nojalla tai muulla tavoin viranomaisen suorittamaan tutkimukseen liittyen; <br>(b) RADA-alustan k&auml;ytt&ouml;ehtojen toimeenpanoa varten; <br>(c) RADA-alustan ja sen palvelujen k&auml;ytt&auml;jien turvallisuuden suojaamiseksi. </span></p><a id="id.30j0zll"></a>
  <p class="c0"><span class="c5 c4">6. EV&Auml;STEET JA VASTAAVAT TEKNIIKAT</span></p>
  <p class="c0"><span class="c4 c2">6.1. </span><span class="c1">RADA k&auml;ytt&auml;&auml; ev&auml;steit&auml; ja vastaavia tekniikoita. Ev&auml;steet (cookie) ovat pieni&auml; tekstitiedostoja, jotka ladataan k&auml;ytt&auml;j&auml;n laitteelle, kun k&auml;ytt&auml;j&auml; vierailee verkkosivulla. Ev&auml;steiden avulla palvelimemme tunnistavat k&auml;ytt&auml;j&auml;n ja t&auml;m&auml;n laitteen. K&auml;yt&auml;mme ev&auml;steit&auml; mukautuaksemme k&auml;ytt&auml;j&auml;n tarpeisiin ja parantaaksemme k&auml;ytt&ouml;kokemusta. EU:n ev&auml;stelain mukaisesti RADA-alusta tarvitsee ev&auml;steiden k&auml;ytt&ouml;&auml; varten k&auml;ytt&auml;j&auml;n tietoon perustuvan suostumuksen.</span></p>
  <p class="c0"><span class="c4 c2">Ev&auml;steen ominaisuuksia:</span></p>
  <ol class="c9 lst-kix_list_3-0 start" start="1">
    <li class="c0 c3"><span class="c1">Ev&auml;steen nimi: Esimerkiksi __utma, __utmb, __utmc, __utmv, __utmz ovat Google Analytics -palvelun k&auml;ytt&auml;mien ev&auml;steiden nimi&auml; ja laravel_session on sen ev&auml;steen nimi, jolla RADA tunnistaa todennetut k&auml;ytt&auml;j&auml;t.</span></li>
    <li class="c0 c3"><span class="c1">Tallennetut tiedot:&nbsp;Satunnainen, yksil&ouml;iv&auml; merkkijono, jolla tunnistetaan k&auml;ytt&auml;j&auml;n selain, ajankohdat, joina k&auml;ytt&auml;j&auml; on viime aikoina vieraillut verkkosivulla, sek&auml; markkinointimateriaali tai linkitt&auml;v&auml; sivu, joka johdatti k&auml;ytt&auml;j&auml;n verkkosivulle. </span></li>
    <li class="c0 c3"><span class="c1">Voimassaolon p&auml;&auml;ttyminen: Joillekin ev&auml;steille ei ole m&auml;&auml;ritetty voimassaoloaikaa. Toiset ev&auml;steet taas poistetaan tietyn ajan kuluttua tai kun k&auml;ytt&auml;j&auml; sulkee selaimensa.</span></li>

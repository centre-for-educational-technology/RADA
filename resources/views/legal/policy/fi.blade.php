  <p class="c0"><span class="c4 c14">7. </span><span class="c2">RADA k&auml;ytt&auml;&auml; Secure Sockets Layer (SSL) -protokollaa. Secure Sockets Layer on protokolla, jonka avulla sovellukset voivat v&auml;litt&auml;&auml; tietoja turvatusti ja suojatusti. SSL-varmenteita k&auml;ytt&auml;v&auml;t sovellukset hoitavat turva-avainten l&auml;hetyksen ja vastaanottamisen sek&auml; salaavat ja purkavat v&auml;litetyn tiedon n&auml;iden avainten avulla. T&auml;ll&auml; tavoin kaikki Alustan sis&auml;ll&auml; liikkuva arkaluonteinen tieto pysyy hyvin suojattuna. RADA-alustan yhteisty&ouml;kumppanit k&auml;ytt&auml;v&auml;t samaa SSL-varmennetekniikkaa, kun eri alustojen v&auml;linen viestint&auml; on tarpeen. N&auml;in ollen tiedonsiirto on turvattu paitsi itse RADA-alustan sis&auml;ll&auml;, my&ouml;s RADA-alustan ja yhteisty&ouml;kumppanien alustojen v&auml;lill&auml;.<br></span></p>

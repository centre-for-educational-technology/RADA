  <p class="c0"><span class="c4 c2">4.3</span><span class="c1">. Sinulla on oikeus poistaa k&auml;ytt&auml;j&auml;tilisi Alustalta ja peruuttaa s&auml;hk&ouml;postiviestint&auml;. </span></p>

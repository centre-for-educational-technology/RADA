    <p class="c0"><span class="c5">1. RADA integritetspolicy</span></p>
    <p class="c0"><span class="c4">RADA</span><span class="c2">&nbsp;</span><span class="c3">(plattformen RADA.eu/h&auml;refter &rdquo;Plattformen&rdquo;) hanteras av Tallinns universitet f&ouml;r RADA-projektet. Syftet med denna integritetspolicy &auml;r att f&ouml;rklara hur RADA (&rdquo;RADA&rdquo;, &rdquo;vi&rdquo;) samlar, anv&auml;nder och delar data om dig genom v&aring;r Plattform och v&aring;ra tj&auml;nster. <br>Detta dokument beskriver integritetsaspekterna i RADA, varmed avses Plattformen och d&auml;rtill h&ouml;rande tj&auml;nster. &nbsp;RADA samlar in olika data om anv&auml;ndaren: personlig information, logginformation, information om anv&auml;ndarens handlingar i Plattformen samt ocks&aring; information som inte g&aring;r att knyta till en individ.. </span></p>

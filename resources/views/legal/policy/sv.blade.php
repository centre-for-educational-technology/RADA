    <li class="c0 c1"><span class="c3">Persondata ska vara relevanta f&ouml;r och begr&auml;nsade till det syfte f&ouml;r vilka de samlas in. Data ska vara korrekta, fullst&auml;ndiga och h&aring;llas uppdaterade;</span></li>
